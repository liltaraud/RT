int				init_mtl(t_mtl *m)
{
	m->ns = 0.5;
	m->ni = 0.5;
	((float *)&m->kd)[0] = 0.5;
	((float *)&m->kd)[1] = 0.5;
	((float *)&m->kd)[2] = 0.5;
	((float *)&m->ke)[0] = 0.5;
	((float *)&m->ke)[1] = 0.5;
	((float *)&m->ke)[2] = 0.5;
	return (1);
}
