#include "libscene.h"

t_3v					new_mark(void)
{
	struct s_3v		ret;

	ret.a[0] = 1;
	ret.a[1] = 0;
	ret.a[2] = 0;
	ret.b[0] = 0;
	ret.b[1] = 1;
	ret.b[2] = 0;
	ret.c[0] = 0;
	ret.c[1] = 0;
	ret.c[2] = 1;
	return (ret);
}
