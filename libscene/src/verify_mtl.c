#include "libscene.h"

int				verify_mtl(t_mtl *m)
{
	if (((float *)&m->kd)[0] < 0.0f || ((float *)&m->kd)[0] >= 1.000001 ||
		((float *)&m->kd)[1] < 0.0f || ((float *)&m->kd)[1] >= 1.000001 ||
		((float *)&m->kd)[2] < 0.0f || ((float *)&m->kd)[2] >= 1.000001 ||
		m->ns < 0.0f || m->ns >= 1.000001 || m->ni < 0.0f ||
		m->ni >= 3.000001 || m->ke >= 1.000001 || m->ke < 0.0f)
		return (0);
	return (1);
}
