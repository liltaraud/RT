#include "libft.h"

void		ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	void	*p;

	p = lst->next;
	while (p != NULL)
	{
		p = lst->next;
		(*f)(lst);
		lst = p;
	}
}
