#include "libft.h"

void				ft_bzero(void *s, size_t n)
{
	unsigned char	*ps1;
	int				i;

	i = 0;
	if (!s || !n)
		return ;
	ps1 = s;
	while (n > 0)
	{
		ps1[i] = '\0';
		i++;
		n--;
	}
}
