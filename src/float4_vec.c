#include "rt.h"

t_vec			float4_vec(cl_float4 f)
{
	struct s_vec		ret;

	ret.x = ((float *)&f)[0];
	ret.y = ((float *)&f)[1];
	ret.z = ((float *)&f)[2];
	return (ret);
}
